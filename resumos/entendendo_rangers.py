"""
RANGERS

"""
# valor_de_parada n達o inclusive(inicio_padrao = 0, valor_de_parada -1)
for num in range(11):
    print(f'{num} - ', end='')
print()

# valor_de_parada n達o inclusive(valor_de_inicio, valor_de_parada)
for num in range(2, 5):
    print(f'{num} - ', end='')
print()

# valor_de_parada n達o inclusive(valor_de_inicio, valor_de_parada, passo)
for num in range(2, 17, 2):
    print(f'{num} - ', end='')
print()

# valor_de_parada n達o inclusive(valor_de_inicio, valor_de_parada, passo)
for num in range(10, 0, -1):
    print(f'{num} - ', end='')