"""
PROGRAAÇÃO ORIENTADA A OBJETOS

POO -> É um paradigma de programação que utiliza de mapeamento de objetos do mundo real, para modelos computacionais

Paradigma de Programação -> É a forma/metodologia utilizada para pensar/desenvolver sistemas

Principais elementos da POO:
    - CLasse: modelo do objeto do mundo real sendo representado computacionalmente
    - Atributo: característica do objeto
    - Método: comportamento do objeto (funções)
    - Construtor: método especial para criar os objetos
    - Objeto: Instância da classe
"""
numero = 10
print(numero)
print(type(numero))

nome = 'Jonas'
print(nome)
print(type(nome))


class Produto:
    pass


ps4 = Produto()
print(ps4)
