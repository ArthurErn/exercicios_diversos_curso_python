"""
Peça para o usuário digitar 3 valores inteiros e imprima a soma deles
"""
num1 = int(input('Digite primeiro número: '))

num2 = int(input('Digite segundo número: '))

num3 = int(input('Digite terceito número: '))

result = (num1 + num2 + num3)

print(f'A soma de {num1} + {num2} + {num3} é de: {result}')
